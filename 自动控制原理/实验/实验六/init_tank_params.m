function p = init_tank_params()
% 双容水箱 + 模糊控制 参数
% 单位这里不强求严格工程单位，重点是课程实验可运行、可调参

% 仿真时间
p.dt = 0.05;      % s
p.T  = 120;       % s

% 水箱参数（等效）
p.A1  = 40;       % 1号箱等效截面积
p.A2  = 45;       % 2号箱等效截面积

% 泵增益：q_in = k*u
p.k1 = 1.30;
p.k2 = 1.20;

% 各箱出流系数：q_out ~ c*sqrt(h)
p.c1 = 0.80;
p.c2 = 0.75;

% 两箱耦合流量系数：q12 ~ c12*sign(h1-h2)*sqrt(|h1-h2|)
p.c12 = 0.35;

% 初始液位
p.h0 = [3.0; 3.0];

% 初始泵输入
p.u0 = [1.5; 1.5];

% 泵输入上下限
p.u_min = 0.0;
p.u_max = 5.0;

% --- 模糊控制相关参数 ---
% 归一化论域
p.norm_range = [-3, 3];

% 量化 / 比例因子（后面主要调这几个）
p.Ke  = 0.30;   % 误差缩放
p.Kde = 0.80;   % 误差变化率缩放
p.Ku  = 0.06;   % 控制增量缩放

% 简单耦合补偿系数
p.alpha = 0.12; % e1_eff = e1 + alpha*e2
p.beta  = 0.12; % e2_eff = e2 + beta*e1
end